nextLevel: function() {
    if (this.currentLevel < this.totalLevels - 1) {
        this.currentLevel++;
        const level = this.getCurrentLevel();
        
        // Atualiza configurações
        Object.assign(gameSettings, level.customSettings || {});
        
        // Reseta o estado
        gameState.startPosition = level.startPosition || { x: 100, y: 550 };
        gameState.firstLoad = true;
        gameState.lastCheckpoint = null;
        gameState.collectedLifeKeys = new Set();
        
        // Limpa objetos
        gameState.objects = {
            tiles: [], coins: [], lives: [], checkpoints: [],
            enemies: [], spikes: [], platforms: [], finish: null
        };
        
        return true; // Indica sucesso
    }
    return false; // Não há próximo nível
}
